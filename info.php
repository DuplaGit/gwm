		<div class="lema"> El arte contemporáneo más propositivo de la ciudad </div>
		<div class="info_text">
		Gallery Weekend México es el fin de semana dedicado al arte contemporáneo. 
		A partir de la magnífica oferta y la calidad de la producción artística del país, 
		esta iniciativa de la revista Código propone un programa que se realizará anualmente, 
		durante la última semana de septiembre, con el objetivo de reunir a la comunidad artística 
		y al creciente número de seguidores del arte más reciente. En esta ocasión tendrá lugar del 
		27 al 29. Las galerías más propositivas de la ciudad de México inaugurarán una de sus mejores 
		exposiciones del año la noche del 27. Asimismo extenderán sus horarios de visita: el sábado de 
		11 de la mañana a 7 de la noche y el domingo de 11 a 5 de la tarde.
		Este proyecto se suma a la tendencia que inició en lugares como Berlín, Madrid, Viena, Varsovia, 
		Los Ángeles, Nueva York y Chicago. Así, Gallery Weekend México ofrece un espacio para dialogar y 
		reflexionar sobre la producción y la circulación de la creación contemporánea. Igualmente tiene 
		como meta promocionar el coleccionismo de arte en nuestro territorio. Una fecha imprescindible 
		en la agenda de la capital del país.  
